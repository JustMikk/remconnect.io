							<h3 
		style="text-align:center;"> RemConnect FAQs   Get answers to common queries about registration, job applications, and more.</h3>
												<a tabindex="0">What is the difference between RemConnect and other hiring agencies? </a>
					<p>RemConnect is strictly dedicated to providing opportunites to candidates based in Ethiopia. We have a global outreach and pool of clients from around the world to hire top professionals in Ethiopia.</p>
												<a tabindex="0">Are certifications and degrees required for every position? Do we need prior experience to get hired?</a>
					<p><b>No, certifications and/or degrees can help immensely in boosting your profile but will not definitively determine hiring status. Experience can also aid your hiring process, but lack of experience won’t deter it. Above all, we suggest completing your profile to the best of your ability allowing us to make the best decision for your fit.</b></p>
												<a tabindex="0">Do candidates pay to apply for jobs? </a>
					<p>No! Do not respond to any inquiry asking for payment of any kind. RemConnect will never ask you to pay fees for any reason.</p>
												<a tabindex="0">Is there a specific age limit to apply for jobs? </a>
					<p>Candidates applying to the platform must be at least 18 years old. </p>
												<a tabindex="0">Do candidates directly apply for jobs or do they have to submit their documents and wait? </a>
					<p>A combination of both. Once you submit all required information, you will be given next steps by an administrator. If there are multiple opportunities available coinciding with your skillset, we will collectively register you for all applicable jobs.</p>
												<a tabindex="0">Is a linkedin account necessary for applying?</a>
					<p><b>Yes! Linked is absolutely necessary. A Linked-in account shows us amongst many things, your level of professionalism, expertise, experience, and education.</b></p>
												<a tabindex="0">Do we sign a contract? If so, how long is the contract term?</a>
					<p><b>Yes. A contract will be composed before training along with waivers and disclosures regarding company procedure. Contracts will be renewed based on demand of the company. Internal full time and part time offers will not have a termination date. All other contracts will have termination dates with an option of extension.</b></p>
												<a tabindex="0">How do we get paid?</a>
					<p><b>Payments will be made to your preferred bank of choice. Currency varies by opportunity. You will get a detailed stub that shows your term length, payment amount, bonus/commission amount (if necessary), and any other applicable items.</b></p>
												<a tabindex="0">How long is the hiring process? When should I expect to hear a response?</a>
					<p><b>Turnaround time depends on the position and job availability. Urgent hires can expect to begin training within a month of interviewing. All other opportunities vary in turnaround time.</b></p>
												<a tabindex="0">How is training administered? Where will the training be?</a>
					<p>Training will be held completely virtually. Training usually lasts 2-6 weeks depending on the position and usually determined by our client. We will give more instructions on how training will be administered once you are interviewed and onboarded. </p>
												<a tabindex="0">Where do we upload documents?</a>
					<p><b>All documents should be uploaded in your portal. If it doesn&#8217;t work please email docs to our HR manager bezamariam@remconnect.io</b></p>
